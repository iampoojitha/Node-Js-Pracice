const os = require('os');

console.log(os.uptime())    //the system running time in sec

console.log(os.userInfo())   //  info about current user

console.log(os.type())    // type of the os

console.log(os.release())  // os release

console.log(os.totalmem())  //total memory of os

console.log(os.freemem())    // free memory in os

